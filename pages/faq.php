			<h1>FAQ & Rules</h1>

			<h2>What can I use this service for?</h2>
			<p>You can upload files to this service if you want an easy way to move files between your home and you office. You could also use the site to share a file with someone else.</p>

			<h2>What types of files can I upload?</h2>
			<p>You can upload any types of files you like. You must be the owner of the file or be allowed to upload it. The owner's of this site accept no responsibility for any files uploaded.</p>
			
			<h2>Which upload rules have to be followed?</h2>

			<ul>
				<li>No files with illegal contents, for example pornography, child pornography, racist material and/or unauthorized copies of copyrighted material. This statement of categories is not the completed list.</li>
				<li>We will not tolerate any abuse of our servers for copyright violations and reserve the right to delete any file that has been uploaded and/or distributed in violation of German or international copyrights. Copyright protected content may especially not be distributed to third parties (for example by publishing links to copyright protected files on other websites).</li>
				<li>We will therefore delete any copyright protected file that we find on Internet Forums, warez-sites, etc.</li>
			</ul>
			<h2>What's the maximum file size?</h2>
			<p>Files you upload may not be bigger than 128 MiB</p>

			<h2>What are cookies?</h2>
			<p>This site uses cookies to save the hashed names of all files you upload. This is done to allow you to list the files you have uploaded. No information about your user is saved on the server.</p>

			<h2>How secure is the file password?</h2>
			<p>The file password is saved as an SHA1 hash. At the moment the password is sent in plain text over the internet when it is entered.</p>

			<h2>Can anyone see my files?</h2>
			<p>Only those who knows the link to the file will be able to access it. Guessing links to files should be very hard, although it is not impossible, only very improbable. If you loose the link to the file we have no way of retrieving it for you.</p>

			<h2>How many files may I upload?</h2>
			<p>You may upload as many files as you wish.</p>

			<h2>What's the cost of using this service?</h2>
			<p>None. This service is completely free. New features added in the future may be provided only to paying customers.</p>

			<h2>How long do you store the files?</h2>
			<p>At the moment we store files indefinitely, but this may change in the future. Don't use this site as your only backup of your files.</p>

			<h2>Can I use this directly from my messenger?</h2>
			<p>There is an <a href="extras/UploadBin-adium-1_0.zip">AppleScript for Adium</a>. Unzip the file and double click on the script file to install it. Then all you need to do is type "/ul" (without the quotes) in a chat to open a file chooser and automatically upload a file and send a link.</p>

